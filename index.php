            <h3>Formação</h3>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A formação do Pearl Jam é uma história interessante que começou no início dos anos 90 em Seattle, uma cidade que se tornaria o epicentro do movimento grunge.
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Stone Gossard e Jeff Ament eram membros da banda de rock alternativa Mother Love Bone. Quando o vocalista da banda, Andrew Wood, faleceu em 1990, Gossard e Ament decidiram seguir em frente com um novo projeto. Mike McCready, um guitarrista que já havia tocado com Gossard e Ament em projetos anteriores, se juntou a eles.
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Eddie Vedder, que na época estava vivendo na Califórnia e havia gravado uma fita demo chamada Momma-Son, que acabou nas mãos de Gossard e Ament. Eles ficaram impressionados com a voz e o estilo de Vedder e o convidaram para se juntar à banda.
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Com a adição de Vedder, a banda começou a se formar. Inicialmente, eles eram conhecidos como Mookie Blaylock, nome inspirado no jogador de basquete. No entanto, logo decidiram mudar o nome para Pearl Jam. O nome Pearl Jam é frequentemente dito ter vindo de uma combinação de fatores, incluindo a inspiração do avô de Vedder e o desejo de ter um nome mais distintivo.
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A banda gravou suas primeiras demos e começou a tocar ao vivo em Seattle. A energia e a química entre os membros se mostraram promissoras. Em 1991, eles assinaram contrato com a Epic Records e lançaram seu álbum de estreia, Ten. Este álbum, com faixas como "Alive," "Even Flow" e "Jeremy," rapidamente ganhou popularidade e ajudou a definir o som grunge.
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Matt Cameron se juntou à banda como baterista em 1998, após a saída de Dave Abbruzzese. Cameron já era conhecido por seu trabalho com a banda Soundgarden, e sua adição trouxe uma nova dinâmica à banda.
            </p>
